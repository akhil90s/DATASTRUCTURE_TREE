package Symmetric_Tree;

public class TreeNode {

    int val;
    TreeNode left;
    TreeNode right;

    public TreeNode() {
    }

    public TreeNode(int value) {
        super();
        this.val = value;
    }

    public TreeNode(int value, TreeNode left, TreeNode right) {
        super();
        this.val = value;
        this.left = left;
        this.right = right;
    }

}
